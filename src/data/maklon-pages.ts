export function getMaklonPage(path: string): MaklonPageConfig | undefined {
  return maklonPages.find((p) => p.path === path);
}

export function getMaklonPageBySlug(slug: string): MaklonPageConfig | undefined {
  return maklonPages.find((p) => p.slug === slug);
}

export function getMaklonPageTitle(path: string): string {
  const page = getMaklonPage(path);
  return page?.title || `Jasa Maklon ${path.split('/').filter(Boolean).pop()?.replace(/-/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase())} BPOM & Halal | Dreamlab`;
}

